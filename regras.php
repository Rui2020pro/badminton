			<p>O volante oficial de Badminton tem 16 penas de ganso que pesam 6 gramas e é jogado com uma raquete.<br>Objetivos do Jogo:</p>
			<ul>
				<li>O jogador faz ponto quando o volante tocar no campo do adversário (Ação Ofensiva).</li>
				<li>O jogador para evitar o ponto do adversário tem de impedir que o volante toque no seu campo (Ação Defensiva).</li>
				<li>O primeiro jogador que chegar aos 21 pontos ganha o jogo. </li>
				<li>Caso haja empate(20-20) no final do jogo, o jogador só ganha com 2 pontos de diferença (Ex: 30-28), no entanto, se os jogadores empatarem 29-29, o jogo termina com o "ponto dourado" (30-29). </li>
				<li>O jogo consiste à melhor de três(3 jogos), contudo um jogador só vence se ganhar 2 jogos.</li>
				<li>Existe um intervalo de 2 minutos entre cada jogo e 1 minuto ao fim de 11 pontos. </li>
				<li>Pode ser praticado em singulares (1 jogador de cada lado do campo) ou em pares (2 jogadores de cada lado do campo).
				<li>As singulares podem ser do Tipo Masculino ou do Tipo Feminino.</li>
				<li>As duplas consistem em 2 jogadores do mesmo género (M,M - M,M ou F,F - F,F) ou de género diferente(M,F - M,F)
				</li>
				<li>O campo é dividido em 2 áreas iguais por uma rede e os jogadores tem de bater o volante por cima da rede.
				</li>
				<li>O jogo é dirigido por 13 árbitros (1 Árbitro Geral, 1 Árbitro Principal e 10 Juízes de Linha).</li>
				<li>O campo do jogo nas singulares tem comprimento 13,40 metros e largura 5,18 metros.</li>
				<li>O campo do jogo nas duplas tem comprimento 13,40 metros e largura 6,10 metros.</li>
				<li>Antes do ínicio do jogo, o árbitro realiza o sorteio entre os adversários (O vencedor pode escolher o campo ou o serviço). Após o apito do árbitro, o vencedor do sorteio dá início ao jogo.</li>
				<li>No primeiro serviço, o jogador tem de estar no lado direito do campo e o adversário tem de estar na diagonal sem pisar as linhas.
				</li>
				<li>O jogador nos serviços, na sua zona, tem de estar no lado esquerdo do campo quando tiver uma pontuação ímpar e se tiver uma pontuação par, este tem de estar no lado direito do campo.</li>
				<li>A cabeça da raquete deve estar abaixo do nível da cintura e deve tocar a base do volante </li>
